# Solicitar 2 numeros al usuario y realizar todas las operaciones basicas de una calculadora (+,-,*,/)
# y mostrar por pantalla el resultado 

num1=float(input("Ingrese el primer numero: "))
num2=float(input("Ingrese el segundo numero: "))

suma=num1+num2
print(f"La suma de {num1} y {num2} dio como resultado {suma}")

resta=num1 - num2
print(f"La resta de {num1} y {num2} dio como resultado {resta}")

multi=num1*num2
print(f"La multiplicacion de {num1} y {num2} dio como resultado {multi}")

div=num1/num2
print(f"la division de {num1} y {num2} dio como resultado {div}")